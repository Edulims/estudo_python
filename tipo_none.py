"""
Tipo None

O tipo de dado None em python representa o tipo sem tipo, ou poderia ser conhecido também
como tipo vazio, porém, falar que é um tipo sem tipo é mais apropriado.

Quando utilziar?

- Podemos utilizar None quando querermos criar uma variável e inicializá-la com um tipo sem tipo,
antes de recebermos um valor final.

OBS: tipo None em python é sempre considerado false

"""

numeros = None

print(numeros)
print(type(numeros))

numeros = 1.44, 1.34, 5.67
print(numeros)
print(type(numeros))





